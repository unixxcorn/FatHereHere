ิ่งสุดท้ายที่คุณต้องการทำก็คือลุกขึ้นมาแล้วออกไปวิ่ง แต่คุณจะพบว่าหลังจากใช้เวลาวิ่งเพียงไม่กี่นาที สมองก็จะปลอดปล่อยฮอร์โมนซึ่งช่วยยกระดับอารมณ์ของคุณขึ้น *แต่อย่างไรก็ดียังมีวิธีอื่นๆ ที่รักษาอาการซึมเศร้าได้ดีและเร็วกว่าการออกกำลังกายเช่นการวิ่งนี้</p><p>จะเห็นได้ว่าการวิ่งช่วยซ่อมแซมสุขภาพได้หลายอย่างด้วยกัน แต่ความจริงนี่เป็นเพียงประโยชน์ส่วนน้อยเท่านั้น การวิ่งมีประโยชน์ต่อทั้งร่างกาย จิตใจและจิตวิญญาณ แค่การวิ่งระยะสั้นๆ ก็ทำให้รู้สึกระปรี้กระเปร่าขึ้น มีสมาธิมากขึ้น และมีความสุขในชีวิตมากขึ้นไปด้วย</p></div>
